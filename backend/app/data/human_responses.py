def get_mental_health_response(condition: str, language: str) -> str:
    """
    Get a human-quality mental health response in the specified language.
    Falls back to English if language not available.
    """
    condition_lower = condition.lower()
    
    # Map keywords to conditions
    if any(word in condition_lower for word in ["suicide", "kill", "die", "end my life", "no reason"]):
        response_type = "crisis"
    elif any(word in condition_lower for word in ["depress", "sad", "hopeless", "worthless", "empty"]):
        response_type = "depression"
    elif any(word in condition_lower for word in ["stress", "pressure", "overwhelm", "burden"]):
        response_type = "stress"
    else:
        response_type = "anxiety"  # Default for anxiety, worried, panic, etc.
    
    responses = MENTAL_HEALTH_RESPONSES.get(response_type, MENTAL_HEALTH_RESPONSES["anxiety"])
    
    # Return response in requested language, fallback to English
    return responses.get(language, responses.get("en", ""))


def get_physical_response(symptom: str, language: str) -> str:
    """
    Get a physical symptom response in the specified language.
    Falls back to English if language not available.
    """
    symptom_lower = symptom.lower()
    
    for key, responses in PHYSICAL_SYMPTOM_RESPONSES.items():
        if key in symptom_lower:
            return responses.get(language, responses.get("en", ""))
    
    return ""
